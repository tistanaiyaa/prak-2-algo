
import math
bilA = float(input("masukkan nilai A: "))
bilB = float(input("masukkan nilai B: "))
jumlah = bilA + bilB 
print(f"hasil dari penjumlahan A + B adalah {jumlah}")
selisih = bilA - bilB 
print(f"selisih antara A dan B adalah {selisih}")
kali = bilA * bilB
print(f"jumlah A dikali B adalah {kali} ")
sisa = bilA % bilB
print(f"jumlah sisa pembagian dari hasil A dibagi B adalah {sisa}")
bagi = bilA / bilB
print(f"jumlah A dibagi B adalah {bagi}")
logA = math.log(bilA)
print(f"hasil dari logA adalah {logA}")
pangkat = bilA ** bilB
print(f"hasil A pangkat B adalah {pangkat}")






